import CoreGraphics

enum ButtonSize {
    enum Small {
        static let width: CGFloat = 50
        static let height: CGFloat = 44
    }
    enum Medium {
        static let width: CGFloat = 100
        static let height: CGFloat = 60
    }
    enum Large {
        static let width: CGFloat = 200
        static let height: CGFloat = 80
    }
}
